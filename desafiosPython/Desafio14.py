n = float(input('Digite o valor da temperatura em graus Celsius: '))
print('A temperatura em graus Fahrenheit é {:.2f} °F,\ne a temperatura em Kelvin é: {:.2f} K'.format((n*9/5)+32, n + 273.15))
#Graus Celsius para Fahrenheit: (0 °C × 9/5) + 32 = 32 °F
#Graus Celsius para Kelvin: 0 °C + 273.15 = 273.15 K