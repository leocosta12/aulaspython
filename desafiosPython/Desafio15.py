k = int(input('Informe a kilometragem percorrida com o carro: '))
d = float(input('Informe a quantidade de dias que o carro foi alugado: '))
print('O valor a ser pago pelo aluguel do carro é: {:.2f} reais'.format(0.15*k + 60*d))
#O valor a ser pago pelo aluguel do carro é: 60 reais por dia + 0.15 reais por km rodado
